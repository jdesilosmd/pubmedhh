function related($rid) {

  $utils = "http://eutils.ncbi.nlm.nih.gov/entrez/eutils";
  $db     = "pubmed";
  $report = "abstract";

  $elink = "$utils/elink.fcgi?" .
               "dbfrom=$db&id=$rid&cmd=neighbor&tool=pmhh&email=pubmedhh@nlm.nih.gov";
  $elink_result = file_get_contents($elink);

  $rest = $elink_result;
  $sbegin = strpos($rest,'<Id>');
	$end = strpos($rest,'</Id>',5);
	$rest =substr($rest,$end);
	$sbegin = strpos($rest,'<Id>');
  $i = 0;
  global $page, $from, $outid, $proj, $lang, $t, $logterm, $stopwords;
	global $max_id;
	$max_id = array();
	
  while (!($sbegin===false)) {	
	$end = strpos($rest,'</Id>',5);
	$len=$end-$sbegin-4;
	$tempID= substr($rest, ($sbegin + 4), $len);
	$IDs[$i] = $tempID;
	$rest =substr($rest,$end);
	$sbegin = strpos($rest,'<Id>');
	$i++;
  }

  $Count = $i-1;
 
  $Tpage=ceil($Count/20);

  if ($page > $Tpage) {
	$page = $Tpage;
  }
  if ($page <=0) {
	$page = 1;
  }


  print $Count." related articles for article (PubMed ID: ".$rid.")";
  print "<hr>";
	
  $begin = ($page-1)*20;
	$id = "";
  for($j=$begin; $j<$begin+20; $j++) {
		$id .= $IDs[$j].",";
	}

    $efetch = "$utils/efetch.fcgi?" .
             "db=$db&id=$id&rettype=$report&retmode=xml&tool=pmhh&email=pubmedhh@nlm.nih.gov";
						 
 	  $getxml = new parXML();
  	$getxml->everything($efetch);
		
